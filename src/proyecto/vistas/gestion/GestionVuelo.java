    private void registrarAeropuertoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarAeropuertoActionPerformed
        // TODO add your handling code here:
       int fila = listaAeropuertos.getSelectedRow();
        String ubicacion = (String)ModelTableAeropuertos.getValueAt(fila, 3);
        String avion = (String)ModelTableAeropuertos.getValueAt(fila, 0);      
        if(origenReferencia.getText()!="Ubicacion"){
            destinoReferencia.setText(ubicacion);
            idDestino.setText(avion);
            destinoVuelo.setText(ubicacion);
        }else{
            origenReferencia.setText(ubicacion);
            idOrigen.setText(avion);
            origenVuelo.setText(ubicacion);
        }   
    }//GEN-LAST:event_registrarAeropuertoActionPerformed

