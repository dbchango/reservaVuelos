    private void guardaVueloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardaVueloActionPerformed
        // TODO add your handling code here:
        
        try{
            Vuelo vuelo = new Vuelo();
        Avion avion = avionesController.findAvion(Integer.parseInt(idAvion.getText()));
        vuelo.setIdAvion(avion);
        vuelo.setOrigen(origenReferencia.getText());
        vuelo.setDestino(destinoReferencia.getText());
        vuelo.setFecha(fechaVuelo.getCalendar().getTime());
        vuelo.setVacantes(Integer.parseInt(vacantesVuelo.getText()));
        vuelo.setTiempoEstimado(BigDecimal.valueOf(Double.parseDouble(tiempoEstimadoVuelo.getText())));
        em.getTransaction().begin();
        em.persist(vuelo);
        em.flush();
        idVueloMain =vuelo.getIdVuelo();
        em.getTransaction().commit();
        em.close();
        System.out.println(idVueloMain);

        RefAeropuert refOrigen = new RefAeropuert();
        RefAeropuert refDestino = new RefAeropuert();
        Aeropuerto aeropuertoOrigen = new Aeropuerto();
        Aeropuerto aeropuertoDestino = new Aeropuerto();
        aeropuertoOrigen = aeropuertoController.findAeropuerto(Integer.parseInt(idOrigen.getText()));
        aeropuertoDestino = aeropuertoController.findAeropuerto(Integer.parseInt(idDestino.getText()));
        vuelo = vuelosController.findVuelo(idVueloMain);
        //Creacion de la referencia aeropuerto de origen
        refOrigen.setIdAeropuerto(aeropuertoOrigen);
        refOrigen.setIdVuelo(vuelo);
        refOrigen.setNombre("Origen");
        //Creacion de la referencia aeropuerto de destino
        refDestino.setIdAeropuerto(aeropuertoDestino);
        refDestino.setIdVuelo(vuelo);
        refDestino.setNombre("Destino");
        refController.create(refOrigen);
        refController.create(refDestino);
        listarVuelos();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
         
    }//GEN-LAST:event_guardaVueloActionPerformed

