    private void listarVuelos(){
        try{
            String[] modelTitles = {"ID Vuelo", "ID Avion", "Origen", "Destino", "Fecha", "Vacantes", "Tiempo estimado"};
            vuelosModel = new DefaultTableModel(null, modelTitles);
            List<Vuelo> vuelos =  vuelosController.findVueloEntities();
            String[] vuelo;
            vuelo = new String[7];
            for (Vuelo itvuelo : vuelos) {
                vuelo[0] = itvuelo.getIdVuelo().toString();
                vuelo[1] = itvuelo.getIdAvion().toStringId();
                vuelo[2] = itvuelo.getOrigen();
                vuelo[3] = itvuelo.getDestino();
                vuelo[4] = itvuelo.getFecha().toString();
                vuelo[5] = itvuelo.getVacantes().toString();
                vuelo[6] = itvuelo.getTiempoEstimado().toString();
                vuelosModel.addRow(vuelo);
            }
            listaVuelos.setModel(vuelosModel);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    } 
    
    private void listarAeropuertos(){
        try{
            String[] tableTitles = {"Id artopuerto", "Nombre", "Pagina Web", "Ciudad", "Pais"};
            ModelTableAeropuertos = new DefaultTableModel(null, tableTitles);
            List<Aeropuerto> aeropuertos =aeropuertoController.findAeropuertoEntities();
            String[] aeropuerto;
            aeropuerto = new String[5];
            for(Aeropuerto aero : aeropuertos){
                aeropuerto[0]=aero.getIdAeropuerto().toString();
                aeropuerto[1]=aero.getNombreAeropuerto();
                aeropuerto[2]=aero.getPaginaweb();
                aeropuerto[3]=aero.getCiudad();
                aeropuerto[4]=aero.getPais();
                ModelTableAeropuertos.addRow(aeropuerto);
            }
            listaAeropuertos.setModel(ModelTableAeropuertos);
                 
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }
    
    
 
    
    
    private void listarAviones(){
        try{
            List<VistaAviones> vistaAviones =  vistaAvionesController.findVistaAvionesEntities();
            String[] tableTitles = {"ID AVION", "CAPACIDAD", "MARCA", "ID AEROLINEA", "NOMBRE AEROLINEA"};
            ModelTableAviones = new DefaultTableModel(null, tableTitles);
            String[] aviones;
            aviones = new String[5];
            for (VistaAviones avione : vistaAviones) {
                aviones[0] = avione.getIdAvion()+"";
                aviones[1] = (String)avione.getCapacidad().toString();
                aviones[2] = avione.getMarca();
                aviones[3] = avione.getIdAerolinea()+"";
                aviones[4] = avione.getNombre();
                ModelTableAviones.addRow(aviones);
            }
            listaAviones.setModel(ModelTableAviones);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }
    
  

